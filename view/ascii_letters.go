func convert_to_banner(word string) []string {
	full_sentence := make([]string, 6)
	for _, v := range word {
		for i := 0; i < 6; i++ {
			full_sentence[i] = full_sentence[i] + alphabet[string(v)][i]
		}
	}
	return full_sentence
}

